package objects;

import java.util.Objects;

public class Alumni {
	private String name;
	private Integer id;
	
	public Alumni() {
	}

	public Alumni(String name, Integer id) {
		this.name = name;
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	@Override
	public int hashCode() {
		return Objects.hash(id);
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Alumni other = (Alumni) obj;
		return Objects.equals(id, other.id);
	}
	
	

}
